export const LanguageProvider = ({ children }) => {
  const [language, setLanguage] = useState(() => {
    // Prioridad: localStorage > navegador > espaÃ±ol
    const stored = localStorage.getItem('language');
    if (stored && ['es', 'en'].includes(stored)) {
      return stored;
    }
    
    const browserLang = navigator.language.split('-')[0];
    return ['es', 'en'].includes(browserLang) ? browserLang : 'es';
  });

  // Guardar en localStorage
  useEffect(() => {
    localStorage.setItem('language', language);
  }, [language]);

  // FunciÃ³n de traducciÃ³n con interpolaciÃ³n
  const t = (key, params = {}) => {
    const keys = key.split('.');
    
    // Combinar traducciones base con adicionales
    const combinedTranslations = {
      ...translations[language],
      ...additionalTranslations[language]
    };
    
    let value = combinedTranslations;
    
    for (const k of keys) {
      value = value?.[k];
    }
    
    if (!value) {
      console.warn(`âŒ Translation missing for key: ${key} in language: ${language}`);
      return key;
    }
    
    // InterpolaciÃ³n de parÃ¡metros
    if (typeof value === 'string' && Object.keys(params).length > 0) {
      return value.replace(/\{(\w+)\}/g, (match, paramKey) => {
        return params[paramKey] || match;
      });
    }
    
    return value;
  };

  // FunciÃ³n para cambiar idioma
  const changeLanguage = (newLanguage) => {
    console.log('ðŸ”„ Changing language from', language, 'to', newLanguage);
    if (['es', 'en'].includes(newLanguage)) {
      setLanguage(newLanguage);
      console.log('âœ… Language changed to:', newLanguage);
    } else {
      console.warn(`âŒ Unsupported language: ${newLanguage}`);
    }
  };

  // FunciÃ³n para obtener idioma actual
  const getCurrentLanguage = () => language;

  // FunciÃ³n para obtener idiomas disponibles
  const getAvailableLanguages = () => [
    { code: 'es', name: 'EspaÃ±ol', flag: 'ðŸ‡ªðŸ‡¸' },
    { code: 'en', name: 'English', flag: 'ðŸ‡ºðŸ‡¸' }
  ];

  // FunciÃ³n para formatear fechas segÃºn el idioma
  const formatDate = (date, options = {}) => {
    const defaultOptions = {
      year: 'numeric',
      month: 'long',
      day: 'numeric'
    };
    
    return new Intl.DateTimeFormat(language, { ...defaultOptions, ...options }).format(date);
  };

  // FunciÃ³n para formatear nÃºmeros segÃºn el idioma
  const formatNumber = (number, options = {}) => {
    return new Intl.NumberFormat(language, options).format(number);
  };

  // FunciÃ³n para formatear moneda segÃºn el idioma
  const formatCurrency = (amount, currency = 'USD') => {
    return new Intl.NumberFormat(language, {
      style: 'currency',
      currency: currency
    }).format(amount);
  };

  const value = {
    language,
    changeLanguage,
    getCurrentLanguage,
    getAvailableLanguages,
    t,
    formatDate,
    formatNumber,
    formatCurrency
  };

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};

export default LanguageContext;
